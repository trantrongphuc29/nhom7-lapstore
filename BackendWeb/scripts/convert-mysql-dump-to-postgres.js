const fs = require("fs");
const path = require("path");

function q(id) {
  return `"${id}"`;
}

function normalizeColumnDef(line) {
  let out = line;
  out = out.replace(/`([^`]+)`/g, (_, c) => q(c));
  out = out.replace(/\bint\b/gi, "INTEGER");
  out = out.replace(/\btinyint\s*\(\s*1\s*\)/gi, "SMALLINT");
  out = out.replace(/\bmediumtext\b/gi, "TEXT");
  out = out.replace(/\bdatetime\b/gi, "TIMESTAMP");
  out = out.replace(/\bjson\b/gi, "JSONB");
  out = out.replace(/\bAUTO_INCREMENT\b/gi, "GENERATED BY DEFAULT AS IDENTITY");
  out = out.replace(/\bON UPDATE CURRENT_TIMESTAMP\b/gi, "");
  out = out.replace(/\bCOLLATE\s+[a-zA-Z0-9_]+\b/gi, "");
  out = out.replace(/\bCHARACTER SET\s+[a-zA-Z0-9_]+\b/gi, "");
  out = out.replace(/\bUNSIGNED\b/gi, "");
  out = out.replace(/\s+COMMENT\s+'[^']*'/gi, "");
  out = out.replace(
    /^\s*"([^"]+)"\s+enum\(([^)]+)\)\s*(.*)$/i,
    (_m, col, values, rest) =>
      `"${col}" VARCHAR(64) ${String(rest || "").trim()} CHECK ("${col}" IN (${values}))`
  );
  out = out.replace(/\s+/g, " ").trim();
  return out;
}

function convertCreateTableBlock(lines, startIdx) {
  const header = lines[startIdx];
  const m = header.match(/CREATE TABLE IF NOT EXISTS `([^`]+)`/i);
  if (!m) return { converted: [header], endIdx: startIdx };
  const table = m[1];
  const out = [`CREATE TABLE IF NOT EXISTS ${q(table)} (`];
  const extraIndexes = [];
  let i = startIdx + 1;
  for (; i < lines.length; i += 1) {
    const raw = lines[i].trim();
    if (raw.startsWith(")")) {
      break;
    }
    if (!raw) continue;
    if (/^KEY\s+`/i.test(raw)) {
      const km = raw.match(/^KEY\s+`([^`]+)`\s*\(([^)]+)\)/i);
      if (km) {
        const idxName = km[1];
        const cols = km[2]
          .split(",")
          .map((c) => c.replace(/`/g, "").trim())
          .map((c) => q(c))
          .join(", ");
        extraIndexes.push(`CREATE INDEX IF NOT EXISTS ${q(`idx_${table}_${idxName}`)} ON ${q(table)} (${cols});`);
      }
      continue;
    }
    if (/^UNIQUE KEY\s+`/i.test(raw)) {
      const um = raw.match(/^UNIQUE KEY\s+`[^`]+`\s*\(([^)]+)\)/i);
      if (um) {
        const cols = um[1]
          .split(",")
          .map((c) => c.replace(/`/g, "").trim())
          .map((c) => q(c))
          .join(", ");
        out.push(`  UNIQUE (${cols}),`);
      }
      continue;
    }
    if (/^PRIMARY KEY/i.test(raw)) {
      const pm = raw.match(/^PRIMARY KEY\s*\(([^)]+)\)/i);
      if (pm) {
        const cols = pm[1]
          .split(",")
          .map((c) => c.replace(/`/g, "").trim())
          .map((c) => q(c))
          .join(", ");
        out.push(`  PRIMARY KEY (${cols}),`);
      }
      continue;
    }
    if (/^CONSTRAINT /i.test(raw)) {
      out.push(`  ${normalizeColumnDef(raw).replace(/,$/, "")},`);
      continue;
    }
    out.push(`  ${normalizeColumnDef(raw).replace(/,$/, "")},`);
  }
  if (out[out.length - 1].endsWith(",")) {
    out[out.length - 1] = out[out.length - 1].slice(0, -1);
  }
  out.push(");");
  out.push(...extraIndexes);
  return { converted: out, endIdx: i };
}

function convertMysqlDump(sql) {
  const lines = String(sql || "").split(/\r?\n/);
  const out = [];
  for (let i = 0; i < lines.length; i += 1) {
    let line = lines[i];
    const trimmed = line.trim();

    if (!trimmed) {
      out.push("");
      continue;
    }
    if (
      /^\/\*!/i.test(trimmed) ||
      /^SET SQL_MODE/i.test(trimmed) ||
      /^SET time_zone/i.test(trimmed) ||
      /^START TRANSACTION/i.test(trimmed)
    ) {
      continue;
    }
    if (/^COMMIT;?$/i.test(trimmed)) {
      out.push("COMMIT;");
      continue;
    }

    if (/^CREATE TABLE IF NOT EXISTS `[^`]+`/i.test(trimmed)) {
      const { converted, endIdx } = convertCreateTableBlock(lines, i);
      out.push(...converted);
      i = endIdx;
      continue;
    }

    line = line.replace(/`([^`]+)`/g, (_, c) => q(c));
    out.push(line);
  }
  return out.join("\n");
}

function main() {
  const input = process.argv[2] || path.resolve(__dirname, "..", "lapstore.sql");
  const output = process.argv[3] || input;
  const raw = fs.readFileSync(input, "utf8");
  const converted = convertMysqlDump(raw);
  fs.writeFileSync(output, converted, "utf8");
  console.log(`Converted MySQL dump to PostgreSQL SQL: ${output}`);
}

main();
